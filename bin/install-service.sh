#!/usr/bin/env bash
# Install systemd user units for the doc-audit service.
#
# Three units are installed:
#   doc-audit.service               main service (server + tunnel)
#   doc-audit-watchdog.timer        health check every minute
#   doc-audit-pull-mirrors.timer    daily git pull of mirrors at 06:00
#
# After install:
#   * service auto-starts at user login (and at boot, if linger is enabled)
#   * watchdog restarts the service when /v1/health stops responding
#   * mirror repos refresh themselves daily
#
# Usage:
#   bin/install-service.sh <owner>/<repo>[,<owner>/<repo>...]
#                                 # consumer repos whose DOC_AUDIT_SERVER_URL
#                                 # variable is updated on each tunnel start
#
# To survive a full reboot (without staying logged in), the operator must
# also enable linger ONCE:
#     sudo loginctl enable-linger $USER
# The script prints this reminder if linger is off.

set -euo pipefail
source "$(dirname "$0")/_lib.sh"

CONSUMERS="${1:-}"
[[ -z "$CONSUMERS" ]] && die "usage: $0 <owner>/<repo>[,<owner>/<repo>...]"

UNIT_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/systemd/user"
SRC_DIR="$AUDIT_BOT_ROOT/examples/systemd"

mkdir -p "$UNIT_DIR" "$STATE_DIR"

# 1. Write env file with consumer repos. EnvironmentFile parses KEY=value
#    lines, so quote the value to play safe with commas.
cat > "$STATE_DIR/env" <<EOF
# Auto-generated by install-service.sh. Re-run install-service.sh to update.
DOC_AUDIT_CONSUMERS="$CONSUMERS"
EOF
chmod 600 "$STATE_DIR/env"
log "wrote $STATE_DIR/env (consumers=$CONSUMERS)"

# 2. Install unit files.
for unit in doc-audit.service doc-audit-watchdog.service doc-audit-watchdog.timer \
            doc-audit-pull-mirrors.service doc-audit-pull-mirrors.timer; do
  cp "$SRC_DIR/$unit" "$UNIT_DIR/$unit"
  log "installed $UNIT_DIR/$unit"
done

systemctl --user daemon-reload
log "systemd user manager reloaded"

# 3. Enable + start.
systemctl --user enable doc-audit.service              >/dev/null
systemctl --user enable doc-audit-watchdog.timer       >/dev/null
systemctl --user enable doc-audit-pull-mirrors.timer   >/dev/null
log "units enabled at user login"

systemctl --user start doc-audit.service               || die "failed to start doc-audit.service — check 'systemctl --user status doc-audit'"
systemctl --user start doc-audit-watchdog.timer        || true
systemctl --user start doc-audit-pull-mirrors.timer    || true

# 4. Make bin/up.sh fall back to the env var when no flag is given.
#    The unit calls `bin/up.sh --no-pull`; we patch up.sh to honor
#    DOC_AUDIT_CONSUMERS automatically (handled by up.sh itself).

# 5. Print final state.
sleep 2
echo
systemctl --user --no-pager status doc-audit.service | head -15
echo

# 6. Linger reminder.
LINGER="$(loginctl show-user "$USER" 2>/dev/null | awk -F= '/^Linger=/{print $2}' | head -1)"
if [[ "$LINGER" != "yes" ]]; then
  cat <<NOTE

⚠️  Linger is currently off. The service WILL stop when you log out.
   To make it survive a full reboot, run ONCE (needs sudo):

       sudo loginctl enable-linger $USER

   After that, the service comes up on every boot without you logging in.
NOTE
fi

cat <<DONE
Doc Audit is now systemd-managed.

  status:   systemctl --user status doc-audit.service
  logs:     journalctl --user -u doc-audit.service -n 50 -f
  watchdog: systemctl --user list-timers doc-audit-*
  stop:     systemctl --user stop doc-audit.service
  uninstall: bin/uninstall-service.sh

DONE
